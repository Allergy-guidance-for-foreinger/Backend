CREATE TABLE language (
    code VARCHAR(10) PRIMARY KEY,
    name VARCHAR(50) NOT NULL,
    english_name VARCHAR(50) NOT NULL
);

CREATE TABLE school (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    source_url VARCHAR(500) NOT NULL,
    created_at TIMESTAMP NOT NULL
);

CREATE TABLE school_translation (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    school_id BIGINT NOT NULL,
    lang_code VARCHAR(10) NOT NULL,
    name VARCHAR(100) NOT NULL,
    is_auto_translated BOOLEAN NOT NULL DEFAULT TRUE,
    created_at TIMESTAMP NOT NULL,
    updated_at TIMESTAMP NOT NULL,
    CONSTRAINT fk_school_translation_school
        FOREIGN KEY (school_id) REFERENCES school(id),
    CONSTRAINT fk_school_translation_language
        FOREIGN KEY (lang_code) REFERENCES language(code),
    CONSTRAINT uk_school_translation_school_lang
        UNIQUE (school_id, lang_code)
);

CREATE TABLE cafeteria (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    school_id BIGINT NOT NULL,
    name VARCHAR(100) NOT NULL,
    created_at TIMESTAMP NOT NULL,
    CONSTRAINT fk_cafeteria_school
        FOREIGN KEY (school_id) REFERENCES school(id),
    CONSTRAINT uk_cafeteria_school_name
        UNIQUE (school_id, name)
);

CREATE TABLE religious_food_restriction (
    code VARCHAR(30) PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    created_at TIMESTAMP NOT NULL
);

CREATE TABLE ingredient (
    code VARCHAR(50) PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    created_at TIMESTAMP NOT NULL
);

CREATE TABLE allergy (
    code VARCHAR(30) PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    display_order INT NOT NULL,
    created_at TIMESTAMP NOT NULL
);

CREATE TABLE allergy_ingredient (
    allergy_code VARCHAR(30) NOT NULL,
    ingredient_code VARCHAR(50) NOT NULL,
    created_at TIMESTAMP NOT NULL,
    PRIMARY KEY (allergy_code, ingredient_code),
    CONSTRAINT fk_allergy_ingredient_allergy
        FOREIGN KEY (allergy_code) REFERENCES allergy(code),
    CONSTRAINT fk_allergy_ingredient_ingredient
        FOREIGN KEY (ingredient_code) REFERENCES ingredient(code)
);

CREATE TABLE religious_food_restriction_ingredient (
    religious_food_restriction_code VARCHAR(30) NOT NULL,
    ingredient_code VARCHAR(50) NOT NULL,
    created_at TIMESTAMP NOT NULL,
    PRIMARY KEY (religious_food_restriction_code, ingredient_code),
    CONSTRAINT fk_rfri_restriction
        FOREIGN KEY (religious_food_restriction_code)
        REFERENCES religious_food_restriction(code),
    CONSTRAINT fk_rfri_ingredient
        FOREIGN KEY (ingredient_code)
        REFERENCES ingredient(code)
);

CREATE TABLE users (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    school_id BIGINT,
    email VARCHAR(255),
    name VARCHAR(100) NOT NULL,
    language_code VARCHAR(10),
    religious_code VARCHAR(30),
    status VARCHAR(20) NOT NULL,
    role VARCHAR(20) NOT NULL,
    onboarding_completed BOOLEAN NOT NULL DEFAULT FALSE,
    created_at TIMESTAMP NOT NULL,
    updated_at TIMESTAMP NOT NULL,
    deleted_at TIMESTAMP,
    CONSTRAINT fk_users_school
        FOREIGN KEY (school_id) REFERENCES school(id),
    CONSTRAINT fk_users_language
        FOREIGN KEY (language_code) REFERENCES language(code),
    CONSTRAINT fk_users_religious_code
        FOREIGN KEY (religious_code)
        REFERENCES religious_food_restriction(code),
    CONSTRAINT uk_users_email
        UNIQUE (email)
);

CREATE TABLE user_avoided_ingredient (
    user_id BIGINT NOT NULL,
    ingredient_code VARCHAR(50) NOT NULL,
    created_at TIMESTAMP NOT NULL,
    PRIMARY KEY (user_id, ingredient_code),
    CONSTRAINT fk_user_avoided_ingredient_user
        FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE CASCADE,
    CONSTRAINT fk_user_avoided_ingredient_ingredient
        FOREIGN KEY (ingredient_code) REFERENCES ingredient(code)
);

CREATE TABLE user_allergy (
    user_id BIGINT NOT NULL,
    allergy_code VARCHAR(30) NOT NULL,
    created_at TIMESTAMP NOT NULL,
    PRIMARY KEY (user_id, allergy_code),
    CONSTRAINT fk_user_allergy_user
        FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE CASCADE,
    CONSTRAINT fk_user_allergy_allergy
        FOREIGN KEY (allergy_code) REFERENCES allergy(code)
);

CREATE TABLE meal_schedule (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    cafeteria_id BIGINT NOT NULL,
    meal_date DATE NOT NULL,
    meal_type VARCHAR(20) NOT NULL,
    created_at TIMESTAMP NOT NULL,
    CONSTRAINT fk_meal_schedule_cafeteria
        FOREIGN KEY (cafeteria_id) REFERENCES cafeteria(id),
    CONSTRAINT uk_meal_schedule
        UNIQUE (cafeteria_id, meal_date, meal_type)
);

CREATE TABLE menu (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(200) NOT NULL,
    spicy_level BIGINT NOT NULL,
    ai_analysis_status VARCHAR(20) NOT NULL,
    latest_ai_analyzed_at TIMESTAMP,
    created_at TIMESTAMP NOT NULL
);

CREATE TABLE meal_menu (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    meal_schedule_id BIGINT NOT NULL,
    menu_id BIGINT NOT NULL,
    corner_name VARCHAR(100),
    display_order INT NOT NULL,
    ingredient_info_source_type VARCHAR(20) NOT NULL,
    ingredient_info_status VARCHAR(20) NOT NULL,
    created_at TIMESTAMP NOT NULL,
    updated_at TIMESTAMP NOT NULL,
    CONSTRAINT fk_meal_menu_meal_schedule
        FOREIGN KEY (meal_schedule_id) REFERENCES meal_schedule(id),
    CONSTRAINT fk_meal_menu_menu
        FOREIGN KEY (menu_id) REFERENCES menu(id),
    CONSTRAINT uk_meal_menu_display_order
        UNIQUE (meal_schedule_id, display_order)
);

CREATE TABLE menu_ai_analysis (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    menu_id BIGINT NOT NULL,
    status VARCHAR(20) NOT NULL,
    model_name VARCHAR(100),
    model_version VARCHAR(50),
    reason TEXT,
    analyzed_at TIMESTAMP,
    created_at TIMESTAMP NOT NULL,
    CONSTRAINT fk_menu_ai_analysis_menu
        FOREIGN KEY (menu_id) REFERENCES menu(id)
);

CREATE TABLE menu_ai_analysis_ingredient (
    menu_ai_analysis_id BIGINT NOT NULL,
    ingredient_code VARCHAR(50) NOT NULL,
    confidence NUMERIC(5,2),
    created_at TIMESTAMP NOT NULL,
    PRIMARY KEY (menu_ai_analysis_id, ingredient_code),
    CONSTRAINT fk_menu_ai_analysis_ingredient_analysis
        FOREIGN KEY (menu_ai_analysis_id)
        REFERENCES menu_ai_analysis(id),
    CONSTRAINT fk_menu_ai_analysis_ingredient_ingredient
        FOREIGN KEY (ingredient_code)
        REFERENCES ingredient(code)
);

CREATE TABLE meal_menu_confirmed_ingredient (
    meal_menu_id BIGINT NOT NULL,
    ingredient_code VARCHAR(50) NOT NULL,
    confirmed_by_user_id BIGINT NOT NULL,
    confirmed_at TIMESTAMP NOT NULL,
    created_at TIMESTAMP NOT NULL,
    PRIMARY KEY (meal_menu_id, ingredient_code),
    CONSTRAINT fk_meal_menu_confirmed_ingredient_meal_menu
        FOREIGN KEY (meal_menu_id) REFERENCES meal_menu(id),
    CONSTRAINT fk_meal_menu_confirmed_ingredient_ingredient
        FOREIGN KEY (ingredient_code) REFERENCES ingredient(code),
    CONSTRAINT fk_meal_menu_confirmed_ingredient_user
        FOREIGN KEY (confirmed_by_user_id) REFERENCES users(id)
);

CREATE TABLE meal_menu_confirmation_history (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    meal_menu_id BIGINT NOT NULL,
    changed_by_user_id BIGINT NOT NULL,
    action_type VARCHAR(30) NOT NULL,
    target_type VARCHAR(30),
    ingredient_code VARCHAR(50),
    memo VARCHAR(500),
    created_at TIMESTAMP NOT NULL,
    CONSTRAINT fk_meal_menu_confirmation_history_meal_menu
        FOREIGN KEY (meal_menu_id) REFERENCES meal_menu(id),
    CONSTRAINT fk_meal_menu_confirmation_history_user
        FOREIGN KEY (changed_by_user_id) REFERENCES users(id),
    CONSTRAINT fk_meal_menu_confirmation_history_ingredient
        FOREIGN KEY (ingredient_code) REFERENCES ingredient(code)
);

